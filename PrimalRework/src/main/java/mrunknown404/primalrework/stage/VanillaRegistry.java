	public static void overrideStength(Block b, float hardness, float blast) {
		BlockState state = ObfuscationReflectionHelper.getPrivateValue(Block.class, b, "defaultBlockState");
		ObfuscationReflectionHelper.setPrivateValue(AbstractBlockState.class, state, hardness, "destroySpeed");
		ObfuscationReflectionHelper.setPrivateValue(AbstractBlock.class, b, blast, "explosionResistance");
		
		//Unsure if this is neccessary
		AbstractBlock.Properties prop = ObfuscationReflectionHelper.getPrivateValue(AbstractBlock.class, b, "properties");
		prop.strength(hardness, blast);
	}
	
	public static void overrideStength(Block b, float hardnessBlast) {
		overrideStength(b, hardnessBlast, hardnessBlast);
	}
	

	private static final Map<Block, Map<EnumToolType, HarvestInfo>> BLOCK_HARVEST_INFOS = new HashMap<Block, Map<EnumToolType, HarvestInfo>>();
	
	private static final Map<Item, EnumStage> ITEM_STAGES = new HashMap<Item, EnumStage>();
	private static final Map<Item, EnumToolType> ITEM_TOOL_TYPES = new HashMap<Item, EnumToolType>();
	private static final Map<Item, EnumToolMaterial> ITEM_TOOL_MATERIALS = new HashMap<Item, EnumToolMaterial>();
	
	public static HarvestInfo getHarvestInfo(Block block, EnumToolType hand) {
		Map<EnumToolType, HarvestInfo> m0 = BLOCK_HARVEST_INFOS.get(block);
		return m0 == null ? null : m0.get(hand);
	}
	
	public static List<HarvestInfo> getHarvestInfos(Block block) {
		if (!BLOCK_HARVEST_INFOS.containsKey(block)) {
			return null;
		}
		
		List<HarvestInfo> infos = new ArrayList<HarvestInfo>();
		for (HarvestInfo info : BLOCK_HARVEST_INFOS.get(block).values()) {
			infos.add(info);
		}
		return infos;
	}
	
	public static EnumStage getStage(Item item) {
		return ITEM_STAGES.get(item);
	}
	
	public static EnumToolType getToolType(Item item) {
		return ITEM_TOOL_TYPES.get(item);
	}
	
	public static EnumToolMaterial getToolMaterial(Item item) {
		return ITEM_TOOL_MATERIALS.get(item);
	}
	
	public static void registerItem(Item item, EnumStage stage) {
		registerItem(item, stage, EnumToolType.none, EnumToolMaterial.hand);
	}
	
	public static void registerItem(Item item, EnumStage stage, EnumToolType toolType, EnumToolMaterial toolMat) {
		ITEM_STAGES.put(item, stage);
		ITEM_TOOL_TYPES.put(item, toolType);
		ITEM_TOOL_MATERIALS.put(item, toolMat);
	}
	
	public static void registerBlock(Block block, HarvestInfo[] infos) {
		registerBlock(block, null, infos);
	}
	
	/** @param info Should not be null! Only used by the other register block method! */
	public static void registerBlock(Block block, HarvestInfo info, HarvestInfo... infos) {
		Map<EnumToolType, HarvestInfo> infoMap = new HashMap<EnumToolType, HarvestInfo>();
		if (info != null) {
			infoMap.put(info.toolType, info);
		}
		
		for (HarvestInfo i : infos) {
			infoMap.put(i.toolType, i);
		}
		
		BLOCK_HARVEST_INFOS.put(block, infoMap);
	}
	
